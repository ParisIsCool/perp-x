local function SpawnDemProps()
	timer.Simple(1, function()
		for k, v in pairs( ServerProps[game.GetMap()] or {} ) do
			local Entity = ents.Create( "prop_physics" )
			if not IsValid( Entity ) then Error( "prop_physics is null, serverprops.\n" ) end
			Entity:SetModel( v.Model )
			Entity:SetPos( v.Pos )
			Entity:SetAngles( v.Angle )
			Entity:SetModelScale( v.Scale or 1, 0 )
			Entity:SetMoveType( MOVETYPE_NONE )

			Entity:Spawn()
			Entity:Activate()

			Entity.UnBreakable = true
			Entity.UnBurnable = true

			local Physics = Entity:GetPhysicsObject()
			if IsValid( Physics ) then
				Physics:EnableMotion( false )
			end
		end
	end)
end

hook.Add( "InitPostEntity", "ServerProps", function()
	MsgN("Loading Server-side Props...")

	SpawnDemProps()
end)

hook.Add( "OnReloaded", "ServerPropsRe", function()
	MsgN("Reloading Server-side Props...")

	SpawnDemProps()
end)
